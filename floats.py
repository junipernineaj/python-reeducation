# Floats
#
# Should equal 0.2
print (0.1 + 0.1)

# Should equal 0.4
print (0.2 + 0.2)

# Should equal 0.2
print (2 * 0.1)

# Should equal 0.4
print (2 * 0.2)

# Random decimal places

print (0.2 + 0.1)
print (3 * 0.1)

# Two integers divided will always give a float - even if the result is a wholw number
# In this case 2.0

print (4/2)

# Mixing integers and floats will also always give a float

print (1 + 2.0)
print (2 * 3.0)
print (3.0 ** 2)


